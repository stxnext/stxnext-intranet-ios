- (NSString *)tableView:(UITableView *)tableView titleForHeaderInSection:(NSInteger)section
{
    switch (section)
    {
        case 0:
            return @"Upcoming";
            
        case 1:
            return @"Now";

        case 2:
            return @"Completed";

        default:
            return @"";
    }
}

